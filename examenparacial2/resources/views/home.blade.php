    <h1 class="titulo1">Biblioteca Binaes</h1>

    <img class="imagen" src="https://www.seguridad.gob.sv/wp-content/uploads/2023/11/captura221.jpg" alt="">
    <p>La Biblioteca Nacional de El Salvador o simplemente BINAES es una institución pública nacional en El Salvador, fue reconstruida por el Gobierno de El Salvador con la cooperación del Gobierno de la República Popular China el 14 de noviembre de 2023. 
        Se sitúa en el Centro Histórico de San Salvador a un costado de la Plaza Gerardo Barrios, frente a ella se ubica la Catedral Metropolitana de San Salvador y a su izquierda el Palacio Nacional de El Salvador.
    </p>
    <p> 
        A finales de 2021 y principios de 2022 se llevó a cabo la demolición del antiguo edificio de la Biblioteca Nacional Francisco Gavidia para la construcción de una nueva edificación donada por la República Popular China, de acuerdo a un convenio firmado el 20 de septiembre de 2019 por los gobiernos de El Salvador y el país asiático.
    </p>

